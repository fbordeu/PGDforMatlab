        function [ok, xi] = isInElement(obj,xi)
            ok = true;
            switch obj.shape
                case 'EDGE'
                    ok =(xi(1)> -1) && (xi(1)<1);
                    if(ok); return; end;
                    if(xi<-1); xi = -1; return; end
                    if(xi>1);  xi = 1;  return; end
                case 'TRI'
                    % for the points in the negative xi plane
                    if ( xi(1)< 0 )
                        xi(1) = 0;
                        ok = false;
                        if(xi(2)>1)
                            xi(2) = 1;
                            return
                        end
                        if(xi(2)<0)
                            xi(2) = 0;
                            return
                        end
                    end
                    % for the points in the negative eta plane
                    if ( xi(2)< 0 )
                        xi(2) = 0;
                        ok = false;
                        if(xi(1)>1)
                            xi(1) = 1;
                            return
                        end
                        if(xi(1)<0)
                            xi(1) = 0;
                            return
                        end
                    end
                    % point closet to the line y = -x+1
                    
                    xi0  =  (xi(1)-xi(2)+1)/2;
                    eta0 =  (-xi(1)+xi(2)+1)/2;
                    
                    %% lower right coorner
                    if(xi0>1)
                        xi= [1 0];
                        ok = false;
                        return
                    end
                    
                    %% top left coorner
                    if(eta0>1)
                        xi= [0 1];
                        ok = false;
                        return
                    end
                    
                    if(xi0< xi(1))
                        xi = [ xi0 eta0 ];
                        ok = false;
                    end
                    
                    
                case 'QUAD'
                    if(xi(1)<-1); xi(1) = -1; ok = false; end
                    if(xi(1)> 1); xi(1) =  1; ok = false; end
                    
                    if(xi(2)<-1); xi(2) = -1; ok = false; end
                    if(xi(2)> 1); xi(2) =  1; ok = false; end
                    
                case 'HEX'
                    if(xi(1)<-1); xi(1) = -1; ok = false; end
                    if(xi(1)> 1); xi(1) =  1; ok = false; end
                    
                    if(xi(2)<-1); xi(2) = -1; ok = false; end
                    if(xi(2)> 1); xi(2) =  1; ok = false; end
                    
                    if(xi(3)<-1); xi(3) = -1; ok = false; end
                    if(xi(3)> 1); xi(3) =  1; ok = false; end
                otherwise
                    error('ELEMENT::isInElement: unsupported element shape ');
            end
        end
